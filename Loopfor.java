package com.Practicejava.org;

public class Loopfor {

	public static void main(String[] args) {
		int i;
		for(i=0;i<=10;i++)
		{
			if(i%2==0){
				System.out.print(i);
		System.out.println(" is even");
			}
			else{
				System.out.print(i);
			System.out.println(" is odd");
			
			}
			
		}
	}

}
